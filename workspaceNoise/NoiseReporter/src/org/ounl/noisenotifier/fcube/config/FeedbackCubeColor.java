package org.ounl.noisenotifier.fcube.config;

public class FeedbackCubeColor {
	
	private int R;
	private int G;
	private int B;
	
	
	public FeedbackCubeColor(int r, int g, int b) {
		super();
		R = r;
		G = g;
		B = b;
	}

	/**
	 * @return the r
	 */
	public int getR() {
		return R;
	}
	/**
	 * @param r the r to set
	 */
	public void setR(int r) {
		R = r;
	}
	/**
	 * @return the g
	 */
	public int getG() {
		return G;
	}
	/**
	 * @param g the g to set
	 */
	public void setG(int g) {
		G = g;
	}
	/**
	 * @return the b
	 */
	public int getB() {
		return B;
	}
	/**
	 * @param b the b to set
	 */
	public void setB(int b) {
		B = b;
	}
	
	

}
